// OBJETOS
class Producto {
  constructor(
    id,
    nombre,
    descripcion,
    rubro,
    marca,
    precio,
    oferta,
    descuentoPorcentaje,
    precioDescuento,
    imagen,
    imagen_sm
  ) {
    this.id = id;
    this.nombre = nombre;
    this.descripcion = descripcion;
    this.rubro = rubro;
    this.marca = marca;
    this.precio = precio;
    this.oferta = oferta;
    this.descuentoPorcentaje = descuentoPorcentaje;
    this.precioDescuento = precioDescuento;
    this.imagen = imagen;
    this.imagen_sm = imagen_sm;
  }

  // Metodos
}
